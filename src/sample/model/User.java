package sample.model;

public class User {
    public int userID;
    public String userName;
    public String userPassword;

    public User() {
        this.userID = userID;
        this.userName = userName;
        this.userPassword = userPassword;
    }

    /**
     * @return userID
     */
    public int getUserID() {

        return userID;
    }

    /**
     * @return userName
     */
    public String getUserName() {

        return userName;
    }

    /**
     * @return userPassword
     */
    public String getUserPassword() {

        return userPassword;
    }
}
